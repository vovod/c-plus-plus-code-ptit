using namespace std;
typedef long long ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int a[n + 5];
        int max = -9999999;
        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
        }
        for (int i = 0; i < n - 1; i++)
        {
            for (int j = i + 1; j < n; j++)
            {
                if (a[j] - a[i] > max)
                    max = a[j] - a[i];
            }
        }
        if (max <= 0)
            cout << -1 << endl;
        else
            cout << max << endl;
    }
}